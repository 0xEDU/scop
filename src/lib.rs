pub struct MlxT {
    window: *mut c_void,
    context: *mut c_void,
    width: i32,
    height: i32,
    delta_time: f64,
}

#[repr(C)]
pub struct MlxInstanceT {
    x: i32,
    y: i32,
    z: i32,
    enabled: bool,
}

